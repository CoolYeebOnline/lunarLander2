const int KeyEsc = VK_ESCAPE;
const int KeyW = 'W';
const int KeyA = 'A';
const int KeyS = 'S';
const int KeyD = 'D';
const int Key1 = '1';
const int Key2 = '2';
const int Key3 = '3';
const int Key4 = '4';

const float ACCELERATION_RATE = 1.0f;
const float DECELERATION_RATE = 0.6f;

//ENUMS

enum GAME_STATE {
	SPLASH,
	MENU,
	PLAY,
	OPTIONS,
	GAME_OVER,
};

//Game Variables
int playerXPos = 1;
int playerYPos = 5;
float deltaTime = 0.0f;
int frameCounter = 0;
Time previousFrameTime = HiResClock::now();
bool exitGame = false;
GAME_STATE currentGameState = PLAY;
float splashDuration = 0.0f;
bool isAccelerating = false;
float landerAcceleration = 0.0f;
bool hasLanded = false;
bool hasCrashed = false;

//Functions
void update();
void draw();
int ClampInt(int intToClamp, int lowerLimit, int upperLimit);
void WriteImageToBuffer(CHAR_INFO* consoleBuffer, const int charsToPrint[], const int coloursToPrint[], const int imageHeight, const int imageWidth, int imageXPos, int imageYPos);
void ClearScreen(CHAR_INFO* consoleBuffer);
void WriteTextToBuffer(CHAR_INFO* consoleBuffer, std::string stringToPrint, int textXPos, int textYPos);
float ClampFloat(float floatToClamp, float lowerLimit, float upperLimit);

int main()
{
	SetConsoleTitle("Title of my Console Window");
	SetConsoleScreenBufferSize(wHnd, buffersize);

	SetConsoleWindowInfo(wHnd, TRUE, &windowSize);

	while (!exitGame)
	{
		//Calculate Delta time
		Time currentFrameTime = HiResClock::now();
		TimeDiff diff = currentFrameTime - previousFrameTime;
		deltaTime = diff.count();
		frameCounter++;

		if (deltaTime >= (1.0f / FRAME_RATE))
		{
			update();
			//cache the timestamp of this frame
			previousFrameTime = currentFrameTime;
		}

		draw();
	}
	return 0;
}

void update() {
	switch (currentGameState) {

	case SPLASH:
	{
		splashDuration += deltaTime;
		if (splashDuration < 3.0f)
		{
			//Title Screen
			WriteImageToBuffer(consoleBuffer, SplashCharacters, SplashColours, SplashHeight, SplashWidth, (WIDTH / 2) - (SplashWidth / 2), (HEIGHT / 2) - (SplashHeight / 2));
		}
		else {
			currentGameState = MENU;
		}
		break;
	}
	case MENU:
	{
		//clear any previous images
		ClearScreen(consoleBuffer);
		//Main Menu - Print out options
		WriteTextToBuffer(consoleBuffer, "1. PLAY", (WIDTH / 2), (HEIGHT / 2));
		WriteTextToBuffer(consoleBuffer, "2. OPTIONS", (WIDTH / 2), (HEIGHT / 2) + 1);
		WriteTextToBuffer(consoleBuffer, "3. QUIT ", (WIDTH / 2), (HEIGHT / 2) + 2);

		if (GetAsyncKeyState(Key1))
		{
			currentGameState = PLAY;
		}
		if (GetAsyncKeyState(Key2))
		{
			currentGameState = OPTIONS;
		}
		if (GetAsyncKeyState(Key3))
		{
			exitGame = true;
		}
		break;
	}
	case PLAY:
	{
		//take input
		if (GetAsyncKeyState(KeyEsc))
		{
			exitGame = true;
		}
		if(!hasLanded && !hasCrashed)
		{
			if (GetAsyncKeyState(KeyW))
			{
				isAccelerating = true;
			}
			if (GetAsyncKeyState(KeyA))
			{
				--playerXPos;
			}
			if (GetAsyncKeyState(KeyS))
			{
				++playerYPos;
			}
			if (GetAsyncKeyState(KeyD))
			{
				++playerXPos;
			}

			if (isAccelerating) {
				landerAcceleration += (ACCELERATION_RATE * deltaTime);
			}
			else {
				landerAcceleration -= (DECELERATION_RATE * deltaTime);
			}

			//reset acceleration flag
			isAccelerating = false;

			landerAcceleration = ClampFloat(landerAcceleration, 0.0f, 1.5f);

			if (landerAcceleration >= 1.0f) // remove this later and put it at the top
			{
				--playerYPos;
			}
			else if (landerAcceleration < 0.5f)
			{
				++playerYPos;
			}
			//clamp input
			playerXPos = ClampInt(playerXPos, 0, (WIDTH - PlayerWidth));
			playerYPos = ClampInt(playerYPos, 0, (HEIGHT - PlayerHeight));

			//gets the two characters underneath the character array
			char bottomLeftchar = BackGroundCharacters[playerXPos + WIDTH * (playerYPos + (PlayerHeight - 1))];
			char bottomRightchar = BackGroundCharacters[(playerXPos + (PlayerWidth - 1)) + WIDTH * (playerYPos + (PlayerHeight - 1))];

			//check to either land or crash
			//check for landing
			if (bottomLeftchar != ' ' || bottomRightchar != ' ')
			{
				if (bottomLeftchar == '_' && bottomRightchar == '_')
				{
					hasLanded = true;
				}
				//check to see it the lander hits something that isn't a landing zone
				else if (bottomLeftchar != ' ' || bottomRightchar != ' ')
				{
					hasCrashed = true;
				}
			}
		}

		//clears the previous frame of animation before drawing the next one
		ClearScreen(consoleBuffer);

		//Draw Background Image
		WriteImageToBuffer(consoleBuffer, BackGroundCharacters, nullptr, HEIGHT, WIDTH, 0, 0);


		//Draw Player Image
		WriteImageToBuffer(consoleBuffer, PlayerCharacters, PlayerColours, PlayerHeight, PlayerWidth, playerXPos, playerYPos);

		//end game text
		if (hasLanded)
		{
			WriteTextToBuffer(consoleBuffer, "Touchdown! You did it!", WIDTH / 2, HEIGHT / 2);
		}
		else if (hasCrashed)
		{
			WriteTextToBuffer(consoleBuffer, "Oops! Lander Down! ", WIDTH / 2, HEIGHT / 2);
		}

		//Draw UI text
		WriteTextToBuffer(consoleBuffer, "SCORE: ", 1, 0);
		WriteTextToBuffer(consoleBuffer, "TIME: ", 1, 1);
		WriteTextToBuffer(consoleBuffer, "FUEL: ", 1, 2);
		break;
	}
	case OPTIONS:
	{
		//options Menu
		break;
	}
	case GAME_OVER:
	{
		//Game over
		break;
	}

	}

}

